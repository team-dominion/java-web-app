Important Linux Commands
========================
- sudo hostname name  = Change OS hostname 
- sudo su -           = Become root 
- sudo -i             = Become root
- sudo su - Jenkins   = Switch user to Jenkins 
- cat /etc/passwd     = Give details about user 
- curl ifconfig.co    = To get public IP 
- hostname -i         = To get the private IP    
- mkdir dev ; cd dev  = Make a directory and cd into it 
- mkdir dev && cd dev = Make a directory and cd into it 
- :wq / :w  / :x      = Save and Exit
- :qa! / :q!          = Exit without changes  
- sudo chown - R ansible:ansible /etc/ansible = Change file ownership from root to ansible 
- chmod 400 fileName  = Change permission of named file (Read Only Permission)
